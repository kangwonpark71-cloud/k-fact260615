/**
 * 입력 텍스트와 최근 거짓뉴스 사례 DB를 매칭합니다.
 * 패턴 매칭 + 카테고리 가중치 기반으로 관련 사례를 반환합니다.
 */
export function matchRecentFakeCases(text: string): RecentFakeNewsCase[] {
  const matched: RecentFakeNewsCase[] = [];
  for (const rule of ALL_RECENT_FAKE_NEWS_CASES) {
    if (rule.patterns.some(p => p.test(text))) {
      matched.push(rule);
    }
  }
  return matched.slice(0, 3);
}

/** AI 프롬프트에 삽입할 최근 거짓뉴스 사례 블록 */
export function formatRecentFakeCasesForPrompt(cases: RecentFakeNewsCase[]): string {
  if (!cases.length) return "";
  const lines = cases.map(
    (c, i) =>
      `${i + 1}. [${c.category}·${c.period}] ${c.title}\n   판정: ${c.verdict} (${c.confidence}% 신뢰)\n   ${c.briefing.slice(0, 150)}\n   출처: ${c.debunked_by}`,
  );
  return `\n[⚠️ 알려진 거짓뉴스 사례 매칭 — 판정 시 반드시 참고]\n${lines.join("\n")}\n`;
}
